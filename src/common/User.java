package common;

public class User {
	private String nome;
	private String cognome;
	private String data_prenotazione;
	private String turno;
	private String num_persone;
	private String num_telefono;
	private String conferma;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getData_prenotazione() {
		return data_prenotazione;
	}
	public void setData_prenotazione(String data_prenotazione) {
		this.data_prenotazione = data_prenotazione;
	}
	public String getTurno() {
		return turno;
	}
	public void setTurno(String turno) {
		this.turno = turno;
	}
	public String getNum_persone() {
		return num_persone;
	}
	public void setNum_persone(String num_persone) {
		this.num_persone = num_persone;
	}
	public String getNum_telefono() {
		return num_telefono;
	}
	public void setNum_telefono(String num_telefono) {
		this.num_telefono = num_telefono;
	}
	public String getConferma() {
		return conferma;
	}
	public void setConferma(String conferma) {
		this.conferma = conferma;
	}
	

}
